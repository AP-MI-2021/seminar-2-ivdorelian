# cazul 1
lst1 = [1, 2, 3]
lst2 = lst1
lst1[0] = 100
print(lst2) # [100, 2, 3]

'''
lst1                                lst2
*-------------> [1, 2, 3] <------------* 
'''

# cazul 2
lst1 = [1, 2, 3]
lst2 = lst1[:]
lst1[0] = 100
print(lst2) # [1, 2, 3]

'''
lst1
*-------------> [1, 2, 3]

lst2
*-------------> [1, 2, 3]
'''


# cazul 3
lst1 = [[1], [2], [3]]
lst2 = lst1[:]
lst1[0][0] = 100
print(lst2) # [[100], [2], [3]]

'''
lst1
*-------------> [*,  *,  *]
                 |   |   |
                 v   v   v
                [1] [2] [3]
                 ^   ^   ^
lst2             |   |   |
*-------------> [*,  *,  *]
'''


# cazul 4
from copy import deepcopy
lst1 = [[1], [2], [3]]
lst2 = deepcopy(lst1)
lst1[0][0] = 100
print(lst2) # [[1], [2], [3]]

'''
lst1
*-------------> [*,  *,  *]
                 |   |   |
                 v   v   v
                [1] [2] [3]
   
                [1] [2] [3]
                 ^   ^   ^
lst2             |   |   |
*-------------> [*,  *,  *]
'''